package com.nico.model;

/**
 *
 * @author Ryuuki
 */
public class JenisLayanan {
    private String tujuan;
    private String provinsi;
    private double reg;
    private double ons;
    private double trc;
    private double hds;
    private double sds;
    
    /**
     * @return the reg
     */
    public double getReg() {
        return reg;
    }

    /**
     * @param reg the reg to set
     */
    public void setReg(double reg) {
        this.reg = reg;
    }

    /**
     * @return the ons
     */
    public double getOns() {
        return ons;
    }

    /**
     * @param ons the ons to set
     */
    public void setOns(double ons) {
        this.ons = ons;
    }

    /**
     * @return the trc
     */
    public double getTrc() {
        return trc;
    }

    /**
     * @param trc the trc to set
     */
    public void setTrc(double trc) {
        this.trc = trc;
    }

    /**
     * @return the tujuan
     */
    public String getTujuan() {
        return tujuan;
    }

    /**
     * @param tujuan the tujuan to set
     */
    public void setTujuan(String tujuan) {
        this.tujuan = tujuan;
    }

    /**
     * @return the provinsi
     */
    public String getProvinsi() {
        return provinsi;
    }

    /**
     * @param provinsi the provinsi to set
     */
    public void setProvinsi(String provinsi) {
        this.provinsi = provinsi;
    }

    /**
     * @return the hds
     */
    public double getHds() {
        return hds;
    }

    /**
     * @param hds the hds to set
     */
    public void setHds(double hds) {
        this.hds = hds;
    }

    /**
     * @return the sds
     */
    public double getSds() {
        return sds;
    }

    /**
     * @param sds the sds to set
     */
    public void setSds(double sds) {
        this.sds = sds;
    }
}
